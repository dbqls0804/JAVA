package application;

public class IntArray {

	public static void main(String[] args) {
		// 배열 []
		int [] numbers = { 2, 4, 6, 8 };
		
		/* 인덱스 
		 *  0  첫번째
		 *  1  두번째
		 *  2  세번째
		 *  ...
		 * */
		System.out.println(numbers[0]);
		System.out.println(numbers[1]);
		System.out.println(numbers[2]);
		System.out.println(numbers[3]);	
	}
}
