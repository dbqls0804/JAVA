package application;

public class Boolean {

	public static void main(String[] args) {
		// 불린 타입은 참 또는 거짓
		
		//참 또는 거짓
		boolean c1 = false;
		System.out.println("조건 1 : " + c1);
		//크다 작다 비교
		boolean c2 = 4 < 5;
		System.out.println("조건 2 : " + c2);
		//같다(==) 같지 않다(!=)
		boolean c3 = 4 == 5;
		System.out.println("조건 3 : " + c3);
		boolean c4 = 4 != 5;
		System.out.println("조건 4 : " + c4);
	}

}
